package PackageFebruary12;

public class Multiplication {
    public static void main(String[] args) {
        for(int i=1;i<11;i++){
            for(int j=1; j<11; j++){
                System.out.println(i+"    X    "+j+"    =    "+i*j);
            }
            System.out.println("--------------------------------------");

        }
    }
}
